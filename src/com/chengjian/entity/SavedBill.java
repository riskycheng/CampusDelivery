package com.chengjian.entity;

public class SavedBill {
	private String mailNoStr;
	private String receiverNameStr;
	private String companyId;
	private String receiverMobilephoneStr;
	private String adminId;
	private String currentInStoreBillNo;

	public String getMailNoStr() {
		return mailNoStr;
	}

	public void setMailNoStr(String mailNoStr) {
		this.mailNoStr = mailNoStr;
	}

	public String getReceiverNameStr() {
		return receiverNameStr;
	}

	public void setReceiverNameStr(String receiverNameStr) {
		this.receiverNameStr = receiverNameStr;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getReceiverMobilephoneStr() {
		return receiverMobilephoneStr;
	}

	public void setReceiverMobilephoneStr(String receiverMobilephoneStr) {
		this.receiverMobilephoneStr = receiverMobilephoneStr;
	}

	public String getAdminId() {
		return adminId;
	}

	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}

	public String getCurrentInStoreBillNo() {
		return currentInStoreBillNo;
	}

	public void setCurrentInStoreBillNo(String currentInStoreBillNo) {
		this.currentInStoreBillNo = currentInStoreBillNo;
	}

}
